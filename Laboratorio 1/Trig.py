import math as mt

class Trig:

    #atributo con el valor numerico de la constante PI
    PI = mt.pi

    def __init__(self) -> None:
        pass

    #funcion que obtiene el seno
    def obtenerSin(self):
        return mt.sin(self.PI)
    
    #funcion que obtiene el coseno
    def obtenerCos(self):
        return mt.cos(self.PI)
    
    #funcion que obtiene el tangente
    def obtenerTan(self):
        return mt.tan(self.PI)
    